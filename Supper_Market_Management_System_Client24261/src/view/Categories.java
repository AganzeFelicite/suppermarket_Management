    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        CategoryDao Obj = new CategoryDao();
        int flag;
        int choice = JOptionPane.showConfirmDialog(this, "delete all??");
        if (choice == JOptionPane.YES_OPTION){
            flag = Obj.deleteAll();
            if(flag >= 1){
            JOptionPane.showMessageDialog(this, "All data deleted successfully");
            table1.setRowCount(0);
            addTableRows();
            }
        }
        if (choice == JOptionPane.NO_OPTION){
           if(categoryId.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Provide the id of a seller to be deleted");             
           }
           flag = Obj.delete(Integer.parseInt(categoryId.getText()));
           if(flag >= 1){
            JOptionPane.showMessageDialog(this, "Row data deleted successfully");
            table1.setRowCount(0);
            addTableRows();
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

