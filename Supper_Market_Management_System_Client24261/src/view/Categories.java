    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        Category Obj = new Category();
        Obj.setCategoryName( categoryName.getText());
        
        CategoryDao dao = new CategoryDao();
        int rowsAffected = dao.createCategory(Obj);
        if (rowsAffected >= 1){
               
                JOptionPane.showMessageDialog(this, "info saved successfully");
                table1.setRowCount(0);
                addTableRows();
        
        }else{
        JOptionPane.showMessageDialog(this, "info not saved");
        }
        
    }//GEN-LAST:event_addActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        //TODO add your handling code here:
         if (categoryId.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "please makesure You enter the ID of the product u wish to update");
        }
        
        Category Obj = new Category();
        Obj.setCategoryId(Integer.parseInt(categoryId.getText()));
        Obj.setCategoryName(categoryName.getText());
        
        CategoryDao dao = new CategoryDao();
        int rowsAffected = dao.updateCategory(Obj);
        if (rowsAffected >= 1){
                
                JOptionPane.showMessageDialog(this, "info updated successfully");
                table1.setRowCount(0);
                addTableRows();
        
        }else{
        JOptionPane.showMessageDialog(this, " not updated");
        }
        
        
        
                            
        
    }//GEN-LAST:event_updateActionPerformed

