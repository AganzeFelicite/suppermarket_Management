     private void addTableRows(){
        SellerDao obj = new SellerDao();
        try{
            ResultSet result = obj.retrieveTableRows();
        while(result.next()){
            table2.addRow(new Object[]{
                result.getInt(1),
                result.getString(2),
                result.getString(3),
                result.getString(4),
                result.getString(5),                             
            });
        }
        }catch(Exception e){
            e.printStackTrace();
        }
   
     
    
    }
