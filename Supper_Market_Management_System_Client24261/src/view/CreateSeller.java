    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        SellerDao obj = new SellerDao();
        int flag;
        int choice = JOptionPane.showConfirmDialog(this, "delete all??");
        if (choice == JOptionPane.YES_OPTION){
            flag = obj.deleteAll();
            if(flag >= 1){
            JOptionPane.showMessageDialog(this, "All data deleted successfully");
            table2.setRowCount(0);
            addTableRows();
            }
        }
        if (choice == JOptionPane.NO_OPTION){
           if(uid.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Provide the id of a seller to be deleted");             
           }
           flag = obj.delete(Integer.parseInt(uid.getText()));
           if(flag >= 1){
            JOptionPane.showMessageDialog(this, "Row data deleted successfully");
            table2.setRowCount(0);
            addTableRows();
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

